#for loop

for i in range(3):                  #for loop using range
    print("hello")

for number in range(3):
    print("greyhound", + number)    #for loop using range printing how many times it iterates through

for x in range(3):
    print("64", + x + 1)            #for loop using range printing how many times it iterates throug but adding + 1 due to python starts to count at 0


#for loop using list
var_list = ["greyhound", "is", "cool"]

